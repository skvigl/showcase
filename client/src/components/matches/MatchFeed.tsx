const getSide = (playerId) => {
  // Берем сумму кодов последних пары символов ID
  const charSum = playerId
    .split("")
    .slice(-2)
    .reduce((acc, char) => acc + char.charCodeAt(0), 0);
  return charSum % 2 === 0 ? "left" : "right";
};

interface GameLog {
  type: "score" | "knockout" | "steal" | "move" | "grab";
  playerId: string;
  targetId?: string;
  toPos?: number;
}

interface LogEntryProps {
  log: GameLog;
  align: "left" | "right";
}

export const LogEntry: React.FC<LogEntryProps> = ({ log, align }) => {
  const isLeft = align === "right";

  const id = <b className="font-bold text-slate-900">{log.playerId.slice(-4)}</b>;
  const targetId = log.targetId ? <b className="font-bold text-slate-900">{log.targetId.slice(-4)}</b> : null;

  let Icon: LucideIcon;
  let iconColor: string;
  let message: React.ReactNode;

  switch (log.type) {
    case "score":
      Icon = Volleyball;
      iconColor = "text-emerald-500 animate-bounce";
      message = [id, " spikes it for a point!"];
      break;
    case "knockout":
      Icon = Swords;
      iconColor = "text-red-500";
      message = [id, " smashed ", targetId, " — Knockout!"];
      break;
    case "steal":
      Icon = Hand;
      iconColor = "text-amber-500";
      message = [id, " dug the ball from ", targetId];
      break;
    case "move":
      Icon = Footprints;
      iconColor = "text-slate-400";
      message = [id, " pushes to ", log.toPos];
      break;
    case "grab":
      Icon = HandHelping;
      iconColor = "text-slate-500";
      message = [id, " picks up the ball"];
      break;
    default:
      return null;
  }

  return (
    <div
      className={`flex items-center gap-3 whitespace-nowrap ${isLeft ? "flex-row-reverse text-right" : "flex-row text-left"}`}
    >
      <Icon size={18} className={`${iconColor} shrink-0`} strokeWidth={2.5} />
      <span className="text-[13px] leading-tight text-slate-600">{message}</span>
    </div>
  );
};

