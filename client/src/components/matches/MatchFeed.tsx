interface RawLog {
  type: "score" | "knockout" | "steal" | "move" | "grab";
  playerId: string | null;
  targetId: string | null;
  position: number;
  tick: number;
}

interface GroupedLog extends RawLog {
  fromPos: number;
  toPos: number;
  lastTick: number;
}

const groupActions = (logs: RawLog[]): GroupedLog[] => {
  const grouped: GroupedLog[] = [];
  const playerPositions: Record<string, number> = {};

  logs.forEach((log) => {
    const last = grouped[grouped.length - 1];
    const prevPos = playerPositions[log.playerId];

    if (log.type === "move" && last && last.type === "move" && last.playerId === log.playerId) {
      // Продолжаем цепочку движения
      last.toPos = log.position;
      last.lastTick = log.tick;
    } else if (log.type === "move" && prevPos === log.position) {
      // Игнорируем движение на месте
      return;
    } else {
      // Создаем новую запись (действие или начало движения)
      grouped.push({
        ...log,
        fromPos: prevPos ?? log.position,
        toPos: log.position,
        lastTick: log.tick,
      });
    }

    // Запоминаем текущую позицию
    playerPositions[log.playerId] = log.position;
  });

  return grouped;
};

